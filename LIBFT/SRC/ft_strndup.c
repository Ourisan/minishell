#include "libft.h"

char	*ft_strndup(const char *src, size_t n)
{
	char	*dest;
	size_t  len;
	size_t  i;

	len = 0;
	while (src[len] && len < n)
		len++;
	dest = (char *)malloc((len + 1) * sizeof(char));
	if (!dest)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

